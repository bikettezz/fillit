#include "libft.h"

char	*ft_strdup(const char *s)
{
	unsigned int	i;
	char			*result;

	i = 0;
	while (s[i])
		i++;
	result = (char*)malloc(sizeof(*result) * i + 1);
	if (result == NULL)
		return (result);
	i = 0;
	while (s[i])
	{
		result[i] = s[i];
		i++;
	}
	result[i] = '\0';
	return (result);
}
