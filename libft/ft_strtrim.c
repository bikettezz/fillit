#include "libft.h"

char	*ft_strtrim(char const *s)
{
	int		i;
	int		len;
	char	*result;

	if (s == NULL)
		return (NULL);
	len = ft_strlen(s);
	while (s[len - 1] == ' ' || s[len - 1] == '\t' || s[len - 1] == '\n')
		len--;
	i = -1;
	while (s[++i] == ' ' || s[i] == '\t' || s[i] == '\n')
		len--;
	if (len < 0)
		len = 0;
	result = ft_strnew(len);
	if (result == NULL)
		return (NULL);
	s = s + i;
	i = 0;
	while (i < len)
		result[i++] = *s++;
	result[i] = '\0';
	return (result);
}
